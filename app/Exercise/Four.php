<?php

namespace App\Exercise;

use Illuminate\Support\Facades\DB;

class Four
{
    /*
     * Implement a function, execute(m, n), which returns an array of the first m multiples of the real number n. Assume that m is a positive integer.
     * Example:
     *  (3, 5) => (5.00, 10.00, 15.00)
     *  (2, 9) => (9.00, 18.00)
     */

    public function execute($m, $n)
    {
        $multi = array();

        for ($i = 1; $i <= $m; $i++) {
            $multi[] = $n * $i;
        }
        
        print_r($multi);

        return $multi;
    }
}
